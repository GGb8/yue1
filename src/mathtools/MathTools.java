package mathtools;

public class MathTools {

	public static int getMax(int a,int b){
		return a>=b?a:b;
	}
	public static int getMin(int a,int b){
		return a<=b?a:b;
	}
	public static int add(int a,int b){
		return a+b;
	}
	public static int div(int a,int b){
		return a-b;
	}







}
